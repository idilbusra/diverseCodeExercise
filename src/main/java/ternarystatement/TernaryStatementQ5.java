package ternarystatement;

public class TernaryStatementQ5 {
    /*
    If the number is positive print ‘Negative’, otherwise print ‘Not Negative’ on the console by
using ternary.
     */
    public static void main(String[] args) {
        int number = 39;
        String result = number>0 ? "Negative" : "Not Negative";
        System.out.println(result);
    }
}
